public class mutators
{
   private int rollno;
    private String name;
    
    public mutators(int rollno, String name) {
        this.rollno = rollno;
        this.name = name;
    }
    public int getRollno() {
        return rollno;
    }
    public void setRollno(int rollno) {
        this.rollno = rollno;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

}
    //getter and setter methods are used to access the private variables of a class.
    //getter method returns the value of the attribute.
    //setter method sets the value of the attribute.
    //output: 10


    
